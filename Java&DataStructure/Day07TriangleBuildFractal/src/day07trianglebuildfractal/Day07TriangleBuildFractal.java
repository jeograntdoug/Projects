    private void formMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseClicked
        int width = triangleFractalCanvas1.getWidth();
        int height = triangleFractalCanvas1.getHeight();
        int mouseX = evt.getX() - 7;// substitude window border
        int mouseY = evt.getY() - 31;// substitude window top bar

        int trueX = startPointX + mouseX;
        int trueY = startPointY + mouseY;

        if(SwingUtilities.isLeftMouseButton(evt)){
            System.out.println("Zoom In");

            triangleFractalCanvas1.setSize(new Dimension((int)(width * scale),(int)(height * scale)));
            
            trueX *= scale;
            trueY *= scale;

            startPointX = trueX - mouseX;
            startPointY = trueY - mouseY;
            
            triangleFractalCanvas1.setLocation(
                    (-1)*startPointX,(-1)*startPointY
            );
        } else if( SwingUtilities.isRightMouseButton(evt)){
            if( width <= this.getWidth() || height <= this.getHeight()){ return; }
            System.out.println("Zoom out");

            triangleFractalCanvas1.setSize(new Dimension((int)(width / scale),(int)(height / scale)));
            
            trueX /= scale;
            trueY /= scale;

            startPointX = trueX - mouseX;
            startPointY = trueY - mouseY;
         
            triangleFractalCanvas1.setLocation(
                    (-1)*startPointX,(-1)*startPointY
            );
        }
    }//GEN-LAST:event_formMouseClicked

